public class Aritmatika{
	public static void main(String[] args){
		int a = 7;
		a++; //tambah 1
		int b = 5;
		b--; //kurang 1
		
		System.out.println("Nilai a=7 Nilai b=5");
		System.out.println();
		System.out.println("Hasil (+) :"+(a+b));
		System.out.println();
		System.out.println("Hasil (-) :"+(a-b));
		System.out.println();
		System.out.println("Hasil (*) :"+(a*b));
		System.out.println();
		System.out.println("Hasil (/) :"+(a/b));
		System.out.println();
		System.out.println("Hasil (%) :"+(a%b));//lambang modulus
		System.out.println();
		System.out.println("Hasil ++ increment:"+(a));	
		System.out.println();
		System.out.println("Hasil -- decrement:"+(b));
	}
}